function add(a,b){
    const add = (a,b)=> a+b;
    return(a+b);
}
function sub(a,b){
    const sub = (a,b)=> a-b;
    return(a-b);
}
function multiply(a,b){
    const multiply = (a,b)=> a*b;
    return(a*b);
}
function divide(a,b){
    const divide = (a,b)=> a/b;
    return(a/b);
}
